/**
 * 瑞幸商品名 → 大 emoji 映射
 * MCP 上游不一定返图, 用关键词匹配给每条菜单贴一个像样的图标, 比 ☕ 通杀好看。
 * 顺序敏感: 越具体的关键词越往前。
 */

const RULES: Array<[RegExp, string]> = [
    // 招牌 / 生椰 / 厚乳系列
    [/生椰|椰云|椰子/i, '🥥'],
    [/厚乳|拿铁/i, '☕'],
    [/美式|浓缩|espresso|意式/i, '☕'],
    [/卡布奇诺|摩卡|玛奇朵|馥芮白|flat\s*white/i, '☕'],
    // 茶饮 / 果茶
    [/茶拿铁|奶茶|轻乳茶/i, '🧋'],
    [/茉莉|乌龙|红茶|绿茶|茶(?!拿)/i, '🍵'],
    [/柠檬|青提|葡萄|橙|柚|莓|桃|芒果|凤梨|菠萝/i, '🧃'],
    // 巧克力 / 可可
    [/巧克力|可可|布朗尼/i, '🍫'],
    // 冷萃 / 气泡
    [/冷萃|气泡|微醺|酒/i, '🫧'],
    // 烘焙 / 轻食
    [/蛋糕|慕斯|提拉米苏/i, '🍰'],
    [/可颂|牛角|面包|吐司/i, '🥐'],
    [/曲奇|饼干|司康/i, '🍪'],
    [/三明治|帕尼尼|轻食|沙拉/i, '🥪'],
    [/蛋挞|挞/i, '🥧'],
    // 周边 / 券
    [/券|卡|兑换|套餐|套票/i, '🎟️'],
    // 兜底: 咖啡店, 默认咖啡杯
];

export const luckinItemEmoji = (name?: string): string => {
    if (!name) return '🥤';
    for (const [re, em] of RULES) {
        if (re.test(name)) return em;
    }
    return '☕';
};
