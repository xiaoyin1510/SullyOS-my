/**
 * 麦当劳商品名 → 大 emoji 映射
 * MCP 上游不返图, 用关键词匹配给每条菜单贴一个像样的图标, 比 🍔 通杀好看。
 * 顺序敏感: 越具体的关键词越往前。
 */

const RULES: Array<[RegExp, string]> = [
    // 早餐
    [/麦满分|帕尼尼|松饼/i, '🥞'],
    [/猪柳|火腿|培根扒/i, '🥓'],
    // 主食类
    [/鳕鱼|深海鱼|鱼柳/i, '🐟'],
    [/鸡翅/i, '🍗'],
    [/鸡腿|鸡块|鸡条/i, '🍗'],
    [/鸡米花|麦乐鸡/i, '🍿'],
    [/汉堡|牛堡|安格斯|板烧|麦辣|双层|汉|堡(?!满)/i, '🍔'],
    // 配餐
    [/中薯|大薯|小薯|薯(?!片)/i, '🍟'],
    [/玉米杯|玉米/i, '🌽'],
    [/沙拉/i, '🥗'],
    [/派|苹果派|香芋派|菠萝派/i, '🥧'],
    // 甜品/小吃
    [/麦旋风|圣代|冰淇淋|甜筒/i, '🍦'],
    [/巧克力|朱古力/i, '🍫'],
    [/草莓/i, '🍓'],
    [/菠萝/i, '🍍'],
    [/曲奇|奥利奥|饼干/i, '🍪'],
    // 饮料
    [/咖啡|拿铁|美式|卡布奇诺|摩卡/i, '☕'],
    [/茶|奶茶|柠檬茶/i, '🧋'],
    [/可乐|雪碧|芬达|汽水|可可|百事/i, '🥤'],
    [/果汁|橙汁|苹果汁/i, '🧃'],
    [/牛奶|酸奶/i, '🥛'],
    // 套餐 (兜底前最后一道, 套餐多半含汉堡)
    [/套餐|超值|全家桶|双人/i, '🍱'],
];

export const mcdItemEmoji = (name?: string): string => {
    if (!name) return '🍽️';
    for (const [re, em] of RULES) {
        if (re.test(name)) return em;
    }
    return '🍽️';
};
